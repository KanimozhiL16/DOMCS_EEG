def save_csv(rows, headers, fname):
    Path(TABLE_DIR).mkdir(parents=True, exist_ok=True)
    path = Path(TABLE_DIR) / f"{fname}.csv"
    with open(str(path), 'w', newline='') as f:
        w = csv.DictWriter(f, fieldnames=headers)
        w.writeheader()
        w.writerows(rows)
    return path


def save_tex(rows, headers, fname, caption="", label=""):
    Path(LATEX_DIR).mkdir(parents=True, exist_ok=True)
    path = Path(LATEX_DIR) / f"{fname}.tex"
    col_fmt = "l" + "r" * (len(headers) - 1)
    lines   = []
    lines.append(r"\begin{table}[!t]")
    lines.append(r"\centering")
    if caption:
        lines.append(f"\\caption{{{caption}}}")
    if label:
        lines.append(f"\\label{{{label}}}")
    lines.append(f"\\begin{{tabular}}{{{col_fmt}}}")
    lines.append(r"\hline")
    lines.append(" & ".join(f"\\textbf{{{h}}}" for h in headers) + r" \\")
    lines.append(r"\hline")
    for row in rows:
        lines.append(" & ".join(str(row.get(h, "—")) for h in headers) + r" \\")
    lines.append(r"\hline")
    lines.append(r"\end{tabular}")
    lines.append(r"\end{table}")
    with open(str(path), 'w') as f:
        f.write("\n".join(lines))
    return path


def load_json(path):
    p = Path(path)
    if not p.exists():
        return None
    with open(p) as f:
        return json.load(f)


# ─── TABLE_01: Per-seed results ───────────────────────────────────────────────

def table01_seed_results():
    summary = load_json(Path(LOG_DIR) / "eval_summary.json")
    if summary is None:
        print("  ⚠ TABLE_01: eval_summary.json not found")
        return

    headers = ["Seed", "EER (%)", "AUC", "Note"]
    rows    = []
    for r in summary.get("per_seed", []):
        rows.append({
            "Seed":   r["seed"],
            "EER (%)": f"{r['eer']:.4f}",
            "AUC":    f"{r['auc']:.4f}",
            "Note":   "Best" if r["seed"] == summary.get("best_seed") else ""
        })
    # Append mean row
    rows.append({
        "Seed": "Mean±SD",
        "EER (%)": f"{summary['mean_eer']:.4f}±{summary['std_eer']:.4f}",
        "AUC":    f"{summary['mean_auc']:.4f}±{summary['std_auc']:.4f}",
        "Note":   f"95% CI [{summary['bootstrap_ci'][0]:.4f}, {summary['bootstrap_ci'][1]:.4f}]"
    })

    save_csv(rows, headers, "TABLE_01_seed_results")
    save_tex(rows, headers, "TABLE_01_seed_results",
             caption="Per-seed B2T evaluation results. Mean±SD over 5 seeds.",
             label="tab:seed_results")
    print("  ✓ TABLE_01")


