# ─── TABLE_06: Disentanglement metrics ────────────────────────────────────────

def table06_disentanglement():
    dis = load_json(Path(LOG_DIR) / "disentanglement_metrics.json")
    headers = ["Metric", "Value", "Expected", "Status"]
    rows    = []

    if dis:
        def avg(key):
            vals = [m[key] for m in dis if m and key in m]
            return np.mean(vals) if vals else None
        def avg_nested(outer, inner):
            vals = [m[outer][inner] for m in dis if m and outer in m]
            return np.mean(vals) if vals else None

        r = [
            ("z_id → Subject probe",   avg_nested("zid_subject_probe","acc"),   "> 0.90", None),
            ("z_id → State probe",     avg_nested("zid_state_probe","acc"),     "≈ chance", None),
            ("z_state → Subject probe",avg_nested("zstate_subject_probe","acc"),"≈ chance", None),
            ("z_state → State probe",  avg_nested("zstate_state_probe","acc"),  "> 0.50", None),
            ("Orthogonality (mean|cos|)", avg("orth_mean_cosine"),              "≈ 0", None),
            ("MI(z_id, Subject)",      avg("mi_zid_subject"),                   "HIGH", None),
            ("MI(z_id, State)",        avg("mi_zid_state"),                     "LOW", None),
            ("Sil(z_id, Subject)",     avg("sil_zid_subject"),                  "> 0", None),
            ("Sil(z_id, State)",       avg("sil_zid_state"),                    "< 0", None),
            ("Sil(z_state, State)",    avg("sil_zstate_state"),                 "> 0", None),
            ("z_id intra-subj cosine", avg("zid_intra_subject_cos"),            "HIGH", None),
            ("z_id inter-subj cosine", avg("zid_inter_subject_cos"),            "≈ 0", None),
        ]
        for (metric, val, expected, _) in r:
            val_str = f"{val:.4f}" if val is not None else "—"
            rows.append({
                "Metric":   metric,
                "Value":    val_str,
                "Expected": expected,
                "Status":   "✓" if val is not None else "—"
            })
    else:
        rows = [{"Metric": "Run 05_disentanglement_analysis.py first",
                 "Value": "—", "Expected": "—", "Status": "—"}]

    save_csv(rows, headers, "TABLE_06_disentanglement_metrics")
    save_tex(rows, headers, "TABLE_06_disentanglement_metrics",
             caption="Disentanglement metrics from linear probes, MI, orthogonality, and silhouette.",
             label="tab:disentangle")
    print("  ✓ TABLE_06")


