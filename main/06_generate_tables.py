# ─── TABLE_04: Per-task results ───────────────────────────────────────────────

def table04_per_task():
    summary = load_json(Path(LOG_DIR) / "eval_summary.json")
    headers = ["Task Group", "Runs", "EER (%)", "AUC", "N probes"]
    rows    = []

    task_info = {
        "RL_Fist_MI":  "R03,R07,R11",
        "BF_Feet_MI":  "R04,R08,R12",
        "RL_Fist_MV":  "R05,R09,R13",
        "BF_Feet_MV":  "R06,R10,R14",
    }

    if summary:
        grp_eers = {}
        grp_aucs = {}
        grp_n    = {}
        for seed_res in summary.get("per_seed", []):
            for grp, val in seed_res.get("per_task_group", {}).items():
                if val.get("eer") is not None:
                    grp_eers.setdefault(grp, []).append(val["eer"])
                    grp_aucs.setdefault(grp, []).append(val.get("auc", np.nan))
                    grp_n[grp] = val.get("n_probes", "—")
        for grp, runs_str in task_info.items():
            eers = grp_eers.get(grp, [])
            aucs = grp_aucs.get(grp, [])
            rows.append({
                "Task Group": grp.replace("_"," "),
                "Runs":       runs_str,
                "EER (%)":    f"{np.mean(eers):.4f}±{np.std(eers):.4f}" if eers else "—",
                "AUC":        f"{np.nanmean(aucs):.4f}" if aucs else "—",
                "N probes":   grp_n.get(grp, "—"),
            })
    else:
        for grp, runs_str in task_info.items():
            rows.append({"Task Group": grp.replace("_"," "), "Runs": runs_str,
                         "EER (%)": "—", "AUC": "—", "N probes": "—"})

    save_csv(rows, headers, "TABLE_04_per_task_results")
    save_tex(rows, headers, "TABLE_04_per_task_results",
             caption="Per-task-group EER — cross-state verification results.",
             label="tab:per_task")
    print("  ✓ TABLE_04")


